#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	unsigned int	i;
	char			*p;

	i = 0;
	p = ft_strdup((char *)s);
	while (p[i])
	{
		p[i] = f(p[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
